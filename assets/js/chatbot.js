(function() {
  let chatHistory = [];

  const chatbotHTML = `
    <button class="chatbot-fab" id="chatbotFab" aria-label="Open Vastu AI Chat" title="Ask Vastu AI">
      <i class="ph ph-chat-circle-dots"></i>
    </button>

    <div class="chatbot-modal" id="chatbotModal">
      <div class="chatbot-header">
        <div class="chatbot-header-avatar">
          <i class="ph ph-sparkle"></i>
        </div>
        <div class="chatbot-header-info">
          <h4>Agni AI</h4>
          <p>Vastu Shastra Expert</p>
        </div>
        <button class="chatbot-close-btn" id="chatbotClose" aria-label="Close Chat"><i class="ph ph-x"></i></button>
      </div>

      <div class="chatbot-messages" id="chatbotMessages">
        <div class="chatbot-msg bot">
          Namaste! I am Agni AI, your Vastu Shastra assistant. Tell me about your space — I can recommend certified remedies to restore elemental balance.
        </div>
      </div>

      <div class="chatbot-input-row">
        <input type="text" id="chatbotInput" placeholder="Describe your Vastu concern..." autocomplete="off">
        <button class="chatbot-send-btn" id="chatbotSend" aria-label="Send message"><i class="ph ph-arrow-circle-right"></i></button>
      </div>

      <div class="chatbot-disclaimer">Responses generated by AI · Verify with a certified Vastu consultant</div>
    </div>
  `;

  document.addEventListener('DOMContentLoaded', () => {
    document.body.insertAdjacentHTML('beforeend', chatbotHTML);

    const fab = document.getElementById('chatbotFab');
    const modal = document.getElementById('chatbotModal');
    const close = document.getElementById('chatbotClose');
    const send = document.getElementById('chatbotSend');
    const input = document.getElementById('chatbotInput');
    const msgs = document.getElementById('chatbotMessages');

    function toggleChat() {
      if (modal.classList.contains('open')) {
        closeChat();
      } else {
        openChat();
      }
    }

    function openChat() {
      modal.classList.add('open');
    }

    function closeChat() {
      modal.classList.remove('open');
    }

    async function sendMessage() {
      const text = input.value.trim();
      if (!text) return;

      input.value = '';
      send.disabled = true;

      appendMessage(text, 'user');
      chatHistory.push({ role: 'user', content: text });

      showTyping();

      try {
        const res = await fetch('/api/chat', {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({ message: text, history: chatHistory.slice(-10) })
        });

        const data = await res.json();
        removeTyping();

        if (data.reply) {
          appendMessage(data.reply, 'bot');
          chatHistory.push({ role: 'assistant', content: data.reply });
        } else {
          appendMessage('I apologize, I could not process that request. Please try again.', 'bot');
        }
      } catch (err) {
        removeTyping();
        appendMessage('Network error. Please check your connection and try again.', 'bot');
        console.error('Chat error:', err);
      } finally {
        send.disabled = false;
        input.focus();
      }
    }

    function appendMessage(text, role) {
      const div = document.createElement('div');
      div.className = `chatbot-msg ${role}`;

      if (role === 'bot') {
        div.innerHTML = formatBotMessage(text);
      } else {
        div.textContent = text;
      }

      msgs.appendChild(div);
      msgs.scrollTop = msgs.scrollHeight;
    }

    function formatBotMessage(text) {
      return text
        .replace(/\n\n/g, '</p><p>')
        .replace(/\n/g, '<br>')
        .replace(/^- (.+)$/gm, '<li>$1</li>')
        .replace(/(<li>.*<\/li>)/s, '<ul>$1</ul>')
        .replace(/<\/ul>\s*<ul>/g, '');
    }

    let typingEl = null;

    function showTyping() {
      typingEl = document.createElement('div');
      typingEl.className = 'chatbot-typing';
      typingEl.innerHTML = '<span></span><span></span><span></span>';
      msgs.appendChild(typingEl);
      msgs.scrollTop = msgs.scrollHeight;
    }

    function removeTyping() {
      if (typingEl) {
        typingEl.remove();
        typingEl = null;
      }
    }

    fab.addEventListener('click', toggleChat);
    close.addEventListener('click', closeChat);
    send.addEventListener('click', sendMessage);
    input.addEventListener('keydown', (e) => {
      if (e.key === 'Enter') sendMessage();
    });
  });
})();
